
var AddCake1 = document.getElementById('cake1_add') ;
var InCart1 = document.getElementById('cake1') ;

AddCake1.addEventListener('click',function(){
    InCart1.classList.toggle('in_cart') ;
    
    for(var i =0 ; i< InCart1.classList.length ; i++){
        if ( InCart1.classList[i] == 'in_cart' ){
            InCart1.innerHTML = "<img src='icons/in_cart.svg' >" ;
        }
        else 
        {
            InCart1.innerHTML = "<img src='icons/cart.svg' >" ;
        }
    }
    
});
